class Dataset_Custom(Dataset):
    """
    Weather 数据集为例
    batch_size 16 默认32
    seq_len 336
    label_len 默认48 start token length
    pred_len 192

    数据1 2 3 4 5 6 7 8 9 10分为:
    1、假设seq_len3:
    数据的形式就为1-2-3, 2-3-4, 3-4-5, 4-5-6, 5-6-7, 6-7-8, 7-8-9, 8-9-10, 9-10-0, 10-0-0（最后两个数据不完整, 进行补零）的10个数据。
    2、假设batch_size为2:
    取出第一个batch为1-2-3, 2-3-4,
    接下来第二个batch为3-4-5, 4-5-6。
    第三个batch为5-6-7, 6-7-8。
    第四个batch为7-8-9, 8-9-10。
    第五个batch为9-10-0, 10-0-0。
    我们的数据一共生成了5个batch。我们把这个玩意儿喂进模型。
    3、网上有说label length是output size, 具体未知
    4、
    """

    def __init__(self, root_path, flag='train', size=None,
                 features='S', data_path='ETTh1.csv',
                 target='OT', scale=True, timeenc=0, freq='h'):
        # size [seq_len, label_len, pred_len]
        # info
        if size == None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]

        # init
        assert flag in ['train', 'test', 'val']
        type_map = {'train': 0, 'val': 1, 'test': 2}
        self.set_type = type_map[flag]

        self.features = features  # M
        self.target = target  # OT target feature in S or MS task
        self.scale = scale  # 用于标准化 用于正则
        self.timeenc = timeenc
        self.freq = freq  # h hourly

        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        # weather为例 seq_len 336
        self.scaler = StandardScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path, self.data_path))
        '''
        df_raw.columns: ['date', ...(other features), target feature]
        '''
        cols = list(df_raw.columns)
        cols.remove(self.target)
        cols.remove('date')
        df_raw = df_raw[['date'] + cols + [self.target]]  # 52696
        # print(cols)
        num_train = int(len(df_raw) * 0.7)  # 36887
        num_test = int(len(df_raw) * 0.2)  # 10539
        num_vali = len(df_raw) - num_train - num_test  # 5270

        # 对应'train': 0, 'val': 1, 'test': 2
        # [0, 36551, 41821]
        border1s = [0, num_train - self.seq_len, len(df_raw) - num_test - self.seq_len]
        # [36887, 42157, 52696]
        border2s = [num_train, num_train + num_vali, len(df_raw)]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]

        if self.features == 'M' or self.features == 'MS':
            # 去掉了date
            cols_data = df_raw.columns[1:]
            df_data = df_raw[cols_data]
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        # 默认为true
        if self.scale:
            train_data = df_data[border1s[0]:border2s[0]]
            # 可以对需要标准化的数据集进行均值、标准差的计算
            self.scaler.fit(train_data.values)
            # print(self.scaler.mean_)
            # exit()
            # transform 才是真正做归一化和标准化的函数, fit 函数只是做了前面的准备工作。
            data = self.scaler.transform(df_data.values)
        else:
            data = df_data.values

        df_stamp = df_raw[['date']][border1:border2]
        # 这里先转换成pd对datetime的形式
        df_stamp['date'] = pd.to_datetime(df_stamp.date)
        if self.timeenc == 0:
            # 方便这里直接获取
            df_stamp['month'] = df_stamp.date.apply(lambda row: row.month, 1)
            df_stamp['day'] = df_stamp.date.apply(lambda row: row.day, 1)
            df_stamp['weekday'] = df_stamp.date.apply(lambda row: row.weekday(), 1)
            df_stamp['hour'] = df_stamp.date.apply(lambda row: row.hour, 1)
            # 生成一个上面month day weekday hour的维度的矩阵
            data_stamp = df_stamp.drop(['date'], 1).values
        elif self.timeenc == 1:
            # 一般主要走这个流程
            # 返回关于时间的矩阵
            data_stamp = time_features(pd.to_datetime(df_stamp['date'].values), freq=self.freq)
            # 1维和0维交换, 转置
            data_stamp = data_stamp.transpose(1, 0)

        self.data_x = data[border1:border2]
        self.data_y = data[border1:border2]
        self.data_stamp = data_stamp

    def __getitem__(self, index):
        """
        如果给类定义了__getitem__方法, 则当按照键取值时, 可以直接返回__getitem__方法执行的结果。
        参数index表示数据和标签在总数据集中的Index
        s_begin 0
        s_end 0+336=336
        r_begin 336-48=288
        r_end 288+48+192=528
        """

        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        # data_x == data_y
        seq_x = self.data_x[s_begin:s_end]
        seq_y = self.data_y[r_begin:r_end]
        # mark是天数矩阵
        seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]

        return seq_x, seq_y, seq_x_mark, seq_y_mark

    def __len__(self):
        # 36887 - 336 - 192 + 1
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    def inverse_transform(self, data):
        # 将标准化后的数据转换为原始数据
        return self.scaler.inverse_transform(data)


class Dataset_Pred(Dataset):
    """
    Weather 数据集为例
    seq_len 336
    label_len 默认48 start token length
    pred_len 192
    """

    def __init__(self, root_path, flag='pred', size=None,
                 features='S', data_path='ETTh1.csv',
                 target='OT', scale=True, inverse=False,
                 timeenc=0, freq='15min', cols=None):
        # size [seq_len, label_len, pred_len]
        # info
        if size == None:
            self.seq_len = 24 * 4 * 4
            self.label_len = 24 * 4
            self.pred_len = 24 * 4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]

        # init
        assert flag in ['pred']

        self.features = features  # M
        self.target = target  # OT target feature in S or MS task
        self.scale = scale
        self.inverse = inverse  # 相比Custom 多了这个属性
        self.timeenc = timeenc
        self.freq = freq  # h hourly
        self.cols = cols  # 相比Custom 多了这个属性 默认None
        self.root_path = root_path
        self.data_path = data_path
        self.__read_data__()

    def __read_data__(self):
        self.scaler = StandardScaler()
        df_raw = pd.read_csv(os.path.join(self.root_path, self.data_path))

        '''
        df_raw.columns: ['date', ...(other features), target feature]
        '''
        # 新-应该是有专门指定cols
        if self.cols:
            cols = self.cols.copy()
            cols.remove(self.target)
        else:
            cols = list(df_raw.columns)
            cols.remove(self.target)
            cols.remove('date')
        df_raw = df_raw[['date'] + cols + [self.target]]  # 52696
        border1 = len(df_raw) - self.seq_len  # 52696-336=52360
        border2 = len(df_raw)  # 52696

        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[1:]
            df_data = df_raw[cols_data]
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        if self.scale:
            self.scaler.fit(df_data.values)
            data = self.scaler.transform(df_data.values)
        else:
            data = df_data.values

        tmp_stamp = df_raw[['date']][border1:border2]
        tmp_stamp['date'] = pd.to_datetime(tmp_stamp.date)

        # 新-根据freq和pred_len 给出未来的时间戳
        # ([ '2021-01-01 06:00:00', '2021-01-01 07:00:00',
        #    '2021-01-01 08:00:00', '2021-01-01 09:00:00',
        #    ...
        #    '2021-01-08 15:00:00', '2021-01-08 16:00:00',
        #    '2021-01-08 17:00:00', '2021-01-08 18:00:00',
        #    dtype='datetime64[ns]', length=193, freq='H')
        pred_dates = pd.date_range(tmp_stamp.date.values[-1], periods=self.pred_len + 1, freq=self.freq)
        df_stamp = pd.DataFrame(columns=['date'])
        # 新-结合 原始date数据和生成的未来date
        df_stamp.date = list(tmp_stamp.date.values) + list(pred_dates[1:])
        if self.timeenc == 0:
            df_stamp['month'] = df_stamp.date.apply(lambda row: row.month, 1)
            df_stamp['day'] = df_stamp.date.apply(lambda row: row.day, 1)
            df_stamp['weekday'] = df_stamp.date.apply(lambda row: row.weekday(), 1)
            df_stamp['hour'] = df_stamp.date.apply(lambda row: row.hour, 1)
            df_stamp['minute'] = df_stamp.date.apply(lambda row: row.minute, 1)
            df_stamp['minute'] = df_stamp.minute.map(lambda x: x // 15)  # 生成一个关于分钟的列
            data_stamp = df_stamp.drop(['date'], 1).values
        elif self.timeenc == 1:
            data_stamp = time_features(pd.to_datetime(df_stamp['date'].values), freq=self.freq)
            data_stamp = data_stamp.transpose(1, 0)

        self.data_x = data[border1:border2]
        if self.inverse:
            # 原始数据
            self.data_y = df_data.values[border1:border2]
        else:
            # 标准化后的数据
            self.data_y = data[border1:border2]
        self.data_stamp = data_stamp

    def __getitem__(self, index):
        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = self.data_x[s_begin:s_end]
        # 使用标准化数据还是原始数据
        if self.inverse:
            seq_y = self.data_x[r_begin:r_begin + self.label_len]
        else:
            seq_y = self.data_y[r_begin:r_begin + self.label_len]
        seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]

        return seq_x, seq_y, seq_x_mark, seq_y_mark

    def __len__(self):
        return len(self.data_x) - self.seq_len + 1

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)

